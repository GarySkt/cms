	<h2 style="color: red;">Oficina T&eacute;cnica Administrativa</h2>
	<p>La oficina técnica administrativa es un órgano de apoyo, dependiente de la Dirección Regional. Está a cargo de un director designado de acuerdo a la normatividad vigente y tiene las siguientes funciones:</p>
	<ul class="ui list">
		<li>Presta asesoramiento y apoyo técnico-legal al Director Regional y demás órganos de la Dirección Regional, en asuntos administrativos, planificación, presupuesto, racionalización y estadística.
</li>
		<li>Proponer a la Dirección lo planes, programas y proyectos institucionales presupuestarios y acciones de racionalización , en coordinación con los órganos de competentes.
</li>
		<li>Formular y evaluar el Plan Estratégico Institucional y el plan de trabajo institucional de la DIRCETUR en coordinación con los órganos de línea.
</li>
		<li>Programar, formular y evaluar proceso presupuestario del sector, así como proponer las modificaciones necesarias en coordinación con la Gerencia Regional de Planeamiento, Presupuesto y Acondicionamiento Territorial del Gobierno Regional de Tacna.
</li>
		<li>Dirigir, ejecutar e implementar las acciones referidas a los Sistemas de Planeamiento Racionalización, Presupuesto, Tesorería, Personal, Abastecimiento y Contabilidad, cautelando el estricto cumplimiento de la normativa vigente, asegurando la racionalidad y unidad de los procesos de administración de recursos.
</li>
		<li>Supervisar y controlar la Ejecución del Calendario de Compromisos aprobado por la instancia correspondiente del MEF, así como ejecutar todas las actividades derivadas del Sistema de Presupuesto en calidad de Unidad Operativa integrante de la Unidad Ejecutora 001 sede Tacna.
</li>
		<li>Mantener informado a la Dirección Regional, sobre el avance en la ejecución presupuestal.
</li>
		<li>Conducir y evaluar acciones de racionalización administrativa para optimizar las funciones de Sector y su desarrollo orgánico.
</li>
		<li>Proporcionar a las unidades orgánicas los recursos materiales, los servicios y equipos que se requieran de acuerdo a las disposiciones legales vigentes.
</li>
		<li>Supervisar y/o ejecutar acciones de control del sistema de administración documentaria, registro y archivo de documentos oficiales.
</li>
		<li>Elaborar y proponer, en coordinación con los distintos órganos de la entidad los instrumentos normativos de gestión, tales como el Rendimiento de Organización y Funciones - ROF, Cuadro para Asignación de Personal - CAP, Presupuesto Analítico de Personal - PAP, Manual de Organización y Funciones - MOF, Directivas Administrativas, Manuales de Procedimientos y otros instrumentos normativos de la entidad, supervisando su correcta aplicación.
</li>
		<li>Recopilar, procesar, analizar y difundir la información estadística de la actividad Turística, Artesanal y Comercio Exterior.
</li>
		<li>Coordinar con la Oficina Departamental de Estadística de Tacna y con la Oficina General de Informática y Estadística del MINCETUR, la determinación de las políticas orientadas para ampliar y/o mejorar la presentación de estadísticas del sector en el nivel regional.
</li>
	</ul>
