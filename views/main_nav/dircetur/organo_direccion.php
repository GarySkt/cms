	<li>Verificar el cumplimiento de las normas de medio ambiente y preservación de los recursos naturales de la región, relacionadas con la actividad turística.
</li>
	<li>Supervisar la correcta aplicación de las normas legales relacionadas con la actividad turística y el cumplimiento de los estándares exigidos a los prestadores de servicios turísticos de la región.
</li>
