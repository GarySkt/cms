<div class="title">
	<h1>Funciones</h1>	
</div>
<br>
<p>La Dirección de Comercio Exterior es un órgano de línea, responsable de ejecutar la política de desarrollo de comercio exterior en la región, así como de promover el desarrollo de una cultura exportadora enmarcada en las políticas nacionales y regionales; que se expiden y se encuentren vigentes. Está a cargo de un Director, designado de acuerdo a la normatividad vigente y tiene las siguientes funciones:</p>
<div class="ui bulleted list">
	<div class="item">Proponer y ejecutar las estrategias y programas de desarrollo de la oferta exportable y de promoción de las exportaciones regionales.</div>
	<div class="item">Brindar asesoramiento y apoyo para el aprovechamiento de los acuerdos o convenios comerciales suscritos por el país, así como de los diferentes esquemas preferenciales.</div>
	<div class="item">Promover el desarrollo de las unidades económicas de la Región, propiciando la competitividad, productividad, asociatividad y difundiendo la cultura exportadora a nivel empresarial en coordinación con los gremios especializados.</div>
	<div class="item">Organizar, sistematizar y facilitar información comercial para la promoción de las exportaciones regionales.</div>
	<div class="item">Coordinar con las instituciones y organismos competentes la identificación, priorización y utilización de mecanismos de promoción de la oferta exportable.</div>
	<div class="item">Promover, proponer y ejecutar estrategias conducentes a la identificación y desarrollo de nueva oferta exportable.</div>
	<div class="item">Ejecutar acciones de capacitación, en coordinación con el Viceministerio de Comercio Exterior, para difundir la temática del comercio exterior.</div>
	<div class="item">Formular, proponer y supervisar la ejecución de planes y programas en materia de comercio exterior a nivel Regional.</div>
	<div class="item">Apoyar la formación de listas de productos de interés regional, para las negociaciones comerciales internacionales.</div>
	<div class="item">Asesorar y difundir los acuerdos comerciales suscritos por el Perú y las preferencias arancelarias contenidas en las listas de productos negociados.</div>
	<div class="item">Asesorar y difundir las preferencias arancelarias otorgadas al Perú, en los esquemas preferenciales de los países desarrollados.</div>
	<div class="item">Promover la participación de expositores regionales en ferias o exposiciones a realizarse en el país o en el exterior, prestando el asesoramiento correspondiente.</div>
	<div class="item">Asesorar al sector exportador y a las diferentes instituciones del sector público y privado de la región en aspectos relacionados con la normativa de origen.</div>
	<div class="item">Aplicar las sanciones administrativas que correspondan, en caso de infracciones, por incumplimiento de kas normas del Sector Comercio Exterior.</div>
	<div class="item">Otras funciones relacionadas con su competencia que le asigne el Director Regional.</div>
	
