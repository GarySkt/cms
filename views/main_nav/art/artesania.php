	<p>La Dirección de Turismo y Artesanía es un órgano de linea, responsable de proponer y ejecutar la política de desarrollo y promoción de la actividad turística y artesanal regional, en el marco de la política nacional de turismo y artesanía; así como calificar y supervisar a los prestadores de servicio turísticos y fomentar las exportaciones de los productos artesanales, con el nivel regional. Está a cargo de un Director, designado de acuerdo a la normatividad vigente.</p>
	<h2 style="color: #FF0000">La Artesania en el Per&uacute;</h2>
	<p>En el Perú, la artesanía es una actividad económica y cultural que involucra a numerosas personas que viven de ella, a través de la cual se trasmite la historia e identidad de los pueblos.Es además una actividad económica en expansión, que involucra alrededor de 100,000 talleres que ocupan directamente a 500,000 personas aproximadamente. La gran mayoría de estos talleres son micro y pequeñas empresas que enfrentan los mismos problemas de formalización, acceso a la información y acceso a mercados que las empresas de otros sectores.
