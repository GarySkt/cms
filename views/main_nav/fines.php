<div class="ui raised very padded text container segment" align="justify">
	<h1 class="title">Fines</h1>
	<p>Son fines de la Dirección Regional de Comercio Exterior y Turismo:</p>
	<ul class="ui list">
		<li class="item">Coadyuvar al desarrollo creciente y sostenido de la región, fomentando el comercio exterior de bienes y servicios, a través de la promoción de una cultura exportadora.</li>
		<li class="item">Promover el desarrollo sostenible de la actividad turística, contribuyendo a la generación de empleo y reducción de la pobreza.</li>
		<li class="item">Revalorizar la actividad artesanal y promover su competitividad, a través de estrategias y acciones orientadas al logro del crecimiento sostenible de la producción y su posicionamiento comercial.</li>
		<li class="item">Preservar a través de la producción artesanal los valores culturales, históricos y la identidad nacional.</li>
	</ul>
</div>
